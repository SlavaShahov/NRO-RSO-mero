-- Миграция 023: помечаем все мероприятия у которых дедлайн уже прошёл
-- как "письмо отправлено" — чтобы при следующем перезапуске не дублировать
--
-- Дедлайн = 3 рабочих дня до мероприятия. Приближённо считаем как
-- event_date - INTERVAL '5 days' (запас на выходные).
-- Точная логика в Go (emailSubWorkdays), но для обратной совместимости
-- достаточно пометить все мероприятия дата которых < сегодня + 5 дней.

UPDATE events
SET email_sent_at = NOW()
WHERE email_sent_at IS NULL
  AND event_date <= CURRENT_DATE + INTERVAL '5 days';